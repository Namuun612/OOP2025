    private void submitBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBTActionPerformed
        // TODO add your handling code here:
        String loc = locationTF.getText();
        String desc = descriptionTA.getText();
        
        if (loc.isEmpty() || desc.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter Location and Description.");
            return;
        }
        
        String severity = "Not set";
        if (lowRB.isSelected()) {
            severity = "Low";
        } else if (mediumRB.isSelected()) {
            severity = "Medium";
        } else if (highRB.isSelected()) {
            severity = "High";
        }
        
        DamageLog log = new DamageLog(loc, desc, severity);
        if (selectedPhotoPath != null && !selectedPhotoPath.isEmpty()) {
            log.addPhoto(selectedPhotoPath);
        }
        
        log.submitLog();
        JOptionPane.showMessageDialog(null, 
                                      "Damage submitted!\nLocation: " + loc +
                                      "\nSeverity: " + severity +
                                      "\nDescription: " + desc);
    }//GEN-LAST:event_submitBTActionPerformed

    private void sendalertBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendalertBTActionPerformed
        // TODO add your handling code here:
        String condition = ((String)weatherConditionCB.getSelectedItem()).trim();
        String severity = (String) severityCB.getSelectedItem();
        
        if (condition== null || condition.isEmpty() || condition.equals("Select one option")){
            alertTA.setText("Please enter a weather condition (e.g. Storm, Flood).");
            return;
        }
        
        AlertSystem alert = new AlertSystem(condition, severity);
        
        AlertSystem.addAlert(alert);
        
        alertTA.setText(alert.getNotificationsText());
        
    }//GEN-LAST:event_sendalertBTActionPerformed

