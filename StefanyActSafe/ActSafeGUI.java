    private void choosePhotoBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_choosePhotoBTActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        int result = chooser.showOpenDialog(null);
        
        if (result == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();
            ImageIcon icon = new ImageIcon(file.getAbsolutePath());
            selectedPhotoPath = file.getAbsolutePath();
            
            Image scaled = icon.getImage().getScaledInstance(
                    photoPreviewLBL.getWidth(),
                    photoPreviewLBL.getHeight(),
                    Image.SCALE_SMOOTH
            );
            
            photoPreviewLBL.setIcon(new ImageIcon(scaled));
            photoPreviewLBL.setText("");
        }
    }//GEN-LAST:event_choosePhotoBTActionPerformed

