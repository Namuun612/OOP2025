    private void searchalertBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchalertBTActionPerformed
        // TODO add your handling code here:
        String cond = JOptionPane.showInputDialog(null,
                "Enter condition to search (e.g. Storm, Flood):");
        
        if (cond == null || cond.trim().isEmpty()) {
            return;
        }
        
        AlertSystem found = AlertSystem.findAlertByCondition(cond);
 
        if (found != null) {
            alertTA.setText(found.getNotificationsText());
        } else {
            JOptionPane.showMessageDialog(null, "No alert found for that condition.");
        }
    }//GEN-LAST:event_searchalertBTActionPerformed

    private void deletealertBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletealertBTActionPerformed
        // TODO add your handling code here:
        String cond = JOptionPane.showInputDialog(null,
                "Enter condition of alert to delete");
        
        if (cond != null && !cond.trim().isEmpty()) {
            boolean removed = AlertSystem.deleteAlertByCondition(cond);
            if (removed) {
            JOptionPane.showMessageDialog(null, "Alert deleted successfully.");
            } else {
                JOptionPane.showMessageDialog(null, "No alert found with that condition.");
            }
        }
    }//GEN-LAST:event_deletealertBTActionPerformed

    private void addshelterBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addshelterBTActionPerformed
        // TODO add your handling code here:
        String county = searchTF.getText();
        
        if (county == null || county.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, " Please enter a county first.");
            return;
        }
        
        String shelterInfo = JOptionPane.showInputDialog(
        null,
        "Enter a shelter Name and Address for " + county + ":"
        );
        
        if (shelterInfo == null || shelterInfo.trim().isEmpty()) {
            return;
        }
        
        Locator custom = new Locator(county.trim(), shelterInfo.trim());
        Locator.addCustomShelter(custom);
        JOptionPane.showMessageDialog(null, "Shelter was saved for " + county.trim() + ".");
    }//GEN-LAST:event_addshelterBTActionPerformed

